package com.dailyuse.model;

public class ProductsVo {
	private int proId;
	private char proName;
	private double proPrice;
	private char proUnit;
	private char proDesc;
	public int getProId() {
		return proId;
	}
	public void setProId(int proId) {
		this.proId = proId;
	}
	public char getProName() {
		return proName;
	}
	public void setProName(char proName) {
		this.proName = proName;
	}
	public double getProPrice() {
		return proPrice;
	}
	public void setProPrice(double proPrice) {
		this.proPrice = proPrice;
	}
	public char getProUnit() {
		return proUnit;
	}
	public void setProUnit(char proUnit) {
		this.proUnit = proUnit;
	}
	public char getProDesc() {
		return proDesc;
	}
	public void setProDesc(char proDesc) {
		this.proDesc = proDesc;
	}
	
}
